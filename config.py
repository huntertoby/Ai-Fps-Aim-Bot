PID_KI = 0.05

# D (微分): 預測未來的偏差，用來減速防過衝 (越大煞車感越重)
PID_KD = 0.05

# PID 取樣時間 (需與 MOUSE_THREAD_SLEEP 匹配或相近)
PID_SAMPLE_TIME = 0.001
# config.py

# ==========================================
# 基礎設定
# ==========================================
# 觸發瞄準的按鍵陣列 (0x01: 左鍵, 0x02: 右鍵, 0x05: 側鍵1, 0x06: 側鍵2)
AIM_KEYS = [0x01, 0x06]

# 切換開關鎖頭模式的按鍵 (預設 0x70 為 F1)
TOGGLE_AIM_KEY = 0x70

# 預設啟動時是否開啟鎖定模式
DEFAULT_AIM_ENABLED = False

# 瞄準部位偏移 (Y軸)
HEAD_OFFSET_Y = 0

# 模型路徑
MODEL_PATH = "models/yolo26l-pose.engine"

# 信心值閾值
CONF_THRESHOLD = 0.4

# 截圖範圍大小
CAPTURE_SIZE = 640

# 截圖目標 FPS (BetterCam)
TARGET_FPS = 1000


# ==========================================
# 滑鼠與 PID 控制設定 (mouse_actor)
# ==========================================
# 死區設定：當目標距離小於此像素時不移動，避免準心小幅度抖動 (預設 1.0)
DEADZONE = 1.0

# 每次迴圈最大的移動像素限制 (防鎖死、防甩槍過頭)
MAX_MOVE_PIXELS = 15

